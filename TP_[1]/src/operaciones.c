/// resta de 2 variables float que devuelve el resultado en float
/// @param num1 num 1 numero float
/// @param num2 otro numero float
/// @return resultado de la resta
float restaDosFloat(float num1, float num2)
{
	float res;
	res = num1 - num2 ;
	return res;
}

